    # Text on left
    oled.text('PHYSICAL', 0, 8)
    oled.text('CONTROL', 5, 20)
    
    # Status bar at bottom
    oled.text('Mode: 0', 0, 50)
    oled.text('Active', 70, 50)
    
    oled.show()

