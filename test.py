def draw_bitmap(oled, x, y, bitmap, width=32, height=32):
    """Draw a 32x32 bitmap on OLED display"""
    bytes_per_row = (width + 7) // 8
    
    for row in range(height):
        for byte_idx in range(bytes_per_row):
            byte_val = bitmap[row * bytes_per_row + byte_idx]
            for bit in range(8):
                pixel_x = x + byte_idx * 8 + bit
                pixel_y = y + row
                
                # Draw if pixel is within display bounds
                if pixel_x < 128 and pixel_y < 64:
                    if byte_val & (1 << (7 - bit)):
                        oled.pixel(pixel_x, pixel_y, 1)

def display_mode0(oled):
    """Mode 0: Physical Control with Gamepad"""
    oled.fill(0)
    
    # Draw gamepad icon on right side
