def display_disabled(oled):
    """Disabled Mode"""
    oled.fill(0)
    
    # Center text
    oled.text('SYSTEM', 20, 15)
    oled.text('DISABLED', 15, 30)
    
    # Draw X mark
    for i in range(12):
        oled.pixel(75 + i, 12 + i, 1)
        oled.pixel(75 + i, 23 - i, 1)
    
    # Status bar
    oled.text('Mode: X', 0, 50)
    oled.text('OFF', 75, 50)
    
    oled.show()

